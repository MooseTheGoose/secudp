#ifndef __SECUDP_UTILITY_H__
#define __SECUDP_UTILITY_H__

#define SECUDP_MAX(x, y) ((x) > (y) ? (x) : (y))
#define SECUDP_MIN(x, y) ((x) < (y) ? (x) : (y))
#define SECUDP_DIFFERENCE(x, y) ((x) < (y) ? (y) - (x) : (x) - (y))

#endif /* __SECUDP_UTILITY_H__ */

